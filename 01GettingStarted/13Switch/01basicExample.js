var i = 9; 
  
switch (i)  { 
    case 0: 
       document.write("i is zero."); 
       break; 
    case 1: 
       document.write("i is one."); 
       break; 
    case 2: 
       document.write("i is two."); 
       break; 
    default: 
       document.write("i is greater than 2."); 
} 
/*
Output : 
i is greater than 2.
*/